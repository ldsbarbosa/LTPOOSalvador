public class Exemplo3 {
    public static void main(String args[]) {
	char letra = 'a';// Caracteres são salvos entre áspas simples.
	String nome = "Salvador Melo";// Strings são salvas entre áspas duplas.
	System.out.println("Caracteres e Strings!");
	System.out.printf("%c\n",letra);
	System.out.printf("%C\n",letra);
	System.out.printf("%s\n",nome);
	System.out.printf("%S\n",nome);
    }
}
